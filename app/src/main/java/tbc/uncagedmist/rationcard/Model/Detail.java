package tbc.uncagedmist.rationcard.Model;

public class Detail {
    private String name, image,id, web;

    public Detail() {
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getWeb() {
        return web;
    }
}
